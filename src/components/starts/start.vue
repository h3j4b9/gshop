<template>
  <div class="star" :class="'star-' + size">
    <span class="star-item" v-for="(item, index) in starClass" :key="index" :class="item"></span>
  </div>
</template>

<script>
export default {
  props: {
    score: {
      type: Number,
    },
    size: {
      type: Number,
    },
  },
  computed: {
    starClass () {
      let {score} = this;
      console.log(score);
      let srcc = [];
      let scoreZhen = Math.floor(score);
      for (let i = 0; i < scoreZhen; i++) {
        srcc.push('on');
      }
      if (score * 10 - scoreZhen * 10 >= 5) {
        srcc.push('half');
      }
      while (srcc.length < 5) {
        srcc.push('off');
      }
      return srcc;
    },
  },
};
</script>

